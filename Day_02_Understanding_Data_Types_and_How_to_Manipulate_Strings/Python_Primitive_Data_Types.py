# Subscripting
print("Hello"[0])

# String
print("123" + "345") # = 123345

# Integer = Whole number
print(123 + 345) # = 468

# Large numbers
print(123_456_789)
print(123456789)

# Float is a data type with numbers with decimals
print(3.14159)

# Boolean is a data type with just two possible values True and False. Note that there are no strings attached (pun not intended) to the True and False values.
print(True)
print(False)