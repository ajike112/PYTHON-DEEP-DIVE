# They are all the same expression in printing "My age"' to be 12.
print("My age: " + ("12"))

print("My age: " + str("12"))

print("My age: " + str(12))

# Addition
print(123 + 456)

# Subtraction
print(7 - 3)

# Multiplication uses astericks *
print(3 * 2)

# Division
# single forward slash symbol / for division gives a float data type answer (preferred)
print(6 / 3)
# Double forward slash symbol // for division gives a interger or whole number data type answer
print(6 // 3)

# Exponential with double astericks **
print(2 ** 3) # This means 2^3 or 2 raised to power 3

# PEMDAS (Parenthesis, Exponential, Multiplication, Division, Addition, Substraction)
print(3 * 3 + 3 / 3 - 3)

print(6 + 4 / 2 - (1 * 2))

