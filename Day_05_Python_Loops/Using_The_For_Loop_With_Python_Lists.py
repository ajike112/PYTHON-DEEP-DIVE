
## LOOPS CONCEPT: Things that have to happen ovver and over again. Loops allow us to tell the computer to repeat actions without having to write repeated code.
# With loops, if we wanted the computer to print out 1 through to 100, it would be very painful to type a print statement for evevry number, or even just typing out all the numbers 1 through to 100. Loops allow us to create a rule and the computer can follow it to do a repeated action.
